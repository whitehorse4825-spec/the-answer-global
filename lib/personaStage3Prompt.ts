export type PersonaStage3Context = {
  userName?: string;
  targetName?: string;
  relationLabel?: string;
  /** 인테이크 재회 유형 — 빙의 톤·무녀 훈수 비중 */
  relation?: RitualRelation;
  /** 1단계와 동일 — 내 말풍선 색 */
  selfBubbleColorHint?: string;
  /** 상대 말풍선 색 */
  otherBubbleColorHint?: string;
  /** 1단계 요약·인용 (선택, 잘라서 전달) */
  stage1Excerpt?: string;
};

export function buildPersonaStage3UserEnvelope(opts: {
  turnIndex: number;
  maxTurns: number;
  context: PersonaStage3Context;
  latestUserMessage: string;
}): string {
  const { turnIndex, maxTurns, context, latestUserMessage } = opts;
  const isFinal = turnIndex >= maxTurns;
  const lines: string[] = [
    "### 이번 요청 메타",
    `- **턴 번호:** ${turnIndex} / ${maxTurns} (${isFinal ? "최종 턴 — 출력 형식은 시스템 지침 4의 「10턴째」만 따른다" : "일반 턴 — [무녀 훈수] 후 [상대 빙의]"})`,
  ];
  if (context.relationLabel?.trim()) {
    lines.push(`- **관계 국면:** ${context.relationLabel.trim()}`);
  }
  if (context.relation) {
    lines.push("");
    lines.push(ritualStage3PersonaHint(context.relation));
  }
  if (context.targetName?.trim()) {
    lines.push(`- **상대 호칭(targetName):** ${context.targetName.trim()}`);
  }
  if (context.selfBubbleColorHint?.trim() || context.otherBubbleColorHint?.trim()) {
    lines.push(
      `- **My_Bubble (애기씨):** ${context.selfBubbleColorHint?.trim() || "(미전달)"}`,
    );
    lines.push(
      `- **Partner_Bubble (상대):** ${context.otherBubbleColorHint?.trim() || "(미전달)"}`,
    );
  }
  if (context.stage1Excerpt?.trim()) {
    const ex = context.stage1Excerpt.trim().slice(0, 6000);
    lines.push("### 1단계 맥락(발췌)");
    lines.push(ex);
  }
  lines.push("### 애기씨의 이번 한마디");
  lines.push(latestUserMessage.trim());
  return lines.join("\n");
}
