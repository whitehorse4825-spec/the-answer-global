import type { RitualRelation } from "@/lib/ritualStorage";

/** [1단계] 카톡·메신저 속마음 분석 — 국면별 가중치 */
export function ritualStage1KakaoHint(rel: RitualRelation): string {
  switch (rel) {
    case "reunion_emergency":
      return [
        "### [1단계 국면 지침 — 긴급 재회 · 심리 분석 위주]",
        "- 상대의 **현재 감정 변화**에 해석·인용·비중의 **약 90%**를 둔다. 막 이별한 직후라 감정 기복·방어·미련이 격하게 섞일 수 있음을 전제로 읽는다.",
        "- 과거 장문 스토리보다 **지금 이 순간** 상대 말풍선(Partner_Bubble)에서 드러나는 **온도·간격·톤의 변화**를 우선 짚는다.",
        "- 금기·팩폭은 애기씨 행동에만 향하되, 이 국면에서는 **감정 자극·선톡 남발·추궁**이 왜 위험한지 **현재 심리 상태**와 연결해 날카롭게 쓴다.",
      ].join("\n");
    case "reunion_revival":
      return [
        "### [1단계 국면 지침 — 오래된 인연 부활 · 연결 고리 위주]",
        "- 과거 대화의 서정만 늘어놓지 말고, **지금도 남아 있는 연결 고리**(연락 채널, 공동 지인, 남은 약속·물건, 말버릇의 잔상 등)를 대화·캡처에서 찾아 **현재형**으로 서술한다.",
        "- ‘예전엔’만 길게 쓰지 말고, **지금 상대가 애기씨를 어떤 위치에 두는지** 추정 근거를 Partner_Bubble 중심으로 잡는다.",
      ].join("\n");
    case "reunion_blocked":
      return [
        "### [1단계 국면 지침 — 차단·연락 두절 · 마지막 패턴 위주]",
        "- 카톡이 끊긴 상태이므로 **마지막 대화 패턴**(누가 끊었는지, 마지막 말의 톤, 읽씹·차단 직전 트리거로 보이는 문장)에서 **차단·연락 단절의 심리적 원인**을 추정한다.",
        "- 대화량이 적어도 **끝맺음 구간**을 돋보이게 읽고, 애기씨·상대 각각의 마지막 발화를 색 태그로 구분해 인용한다.",
      ].join("\n");
    default:
      return ritualStage1KakaoHint("reunion_emergency");
  }
}

/** [2단계] 재회 타로 — 국면별 처방 방향 (점지 2개·시간 선형 등 공통 규칙은 유지) */
